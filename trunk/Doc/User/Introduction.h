#ifndef __INTRODUCTION_H
#define __INTRODUCTION_H

/** @page Introduction Introduction
 * @section org How this manual is organized.
 *
 * This manual is broken up into ``parts'', one for each ``main program''
 * @latexonly
 * \footnote{The various programming libraries are described in the programming
 * guides\cite{progguide}.} 
 * @endlatexonly
 * Each part contains two or more chapters, a tutorial chapter and one or 
 * more reference chapters.  These parts are:
 *   - Chapter @ref univtest documents the Universal Test program. This
 *     program is part of the CMR/I (Chubb) library and is used to test CMR/I
 *     nodes.
 *   - Chapters @ref timetable:ref and @ref timetable:Tutorial document the 
 *     Time Table (V2) program. This program is used to create employee 
 *     timetables.
 *   - Chapter @ref fcf documents the Freight Car Forwarder (V2)
 *     program. This program is used to create switch lists for freight car
 *     forwarding.
 *   - Chapter @ref calc documents the calculator scripts, Resistor and 
 *     LocoPull, that are available to help model railroaders perform some 
 *     common calculations.
 *   - Chapter @ref camera documents the camera scripts. These scripts perform 
 *     various camera scene calculations that are useful for model 
 *     railroaders.
 *   - Chapter @ref dispatch documents the automated dispatcher program.
 */

#endif // __INTRODUCTION_H

